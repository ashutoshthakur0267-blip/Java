/*Java is both a compiled and an interpreted language — it uses a hybrid approach.

✅ Explanation

When you write a Java program, it goes through two stages:

Compilation (by javac)

Your .java source code is compiled into Bytecode (.class file).

This bytecode is not machine-specific — it's platform-independent.

Interpretation (by JVM)

The Java Virtual Machine (JVM) interprets or Just-In-Time compiles the bytecode into machine code at runtime, depending on the system.

✅ Final Answer (For Exams or Viva)

Java is neither purely compiled nor purely interpreted. It uses a hybrid model. First, the Java compiler (javac) compiles the source code into platform-independent bytecode. Then, the JVM interprets or JIT-compiles this bytecode into machine code at runtime. This approach makes Java both portable and efficient.

✅ Why Java Uses This Approach?
Feature	Benefit
Compilation to Bytecode	Fast execution
Interpretation on JVM	Platform independence (Write Once, Run Anywhere) */