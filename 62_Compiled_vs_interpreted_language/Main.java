/*✅ Difference Between Compiled and Interpreted Languages
Basis	Compiled Language	Interpreted Language
Definition	Code is converted into machine code (binary) before execution by a compiler	Code is executed line-by-line by an interpreter during execution
Execution Speed	Faster – because already converted to machine code	Slower – translation happens on the fly
Error Detection	Errors are shown after full compilation	Errors are shown immediately line-by-line
Portability	Machine code is platform-dependent (needs recompilation for another OS)	Source code is platform-independent (runs wherever interpreter is available)
Examples	C, C++, Java (partially compiled to bytecode), Go, Rust	Python, JavaScript, PHP, Ruby, Shell Script
Output File	Generates an executable file (.exe, .out, .class)	No separate output file – executed directly
Best Used For	High-performance applications like games, OS, system software	Scripting, web apps, automation, rapid development
✅ Quick Summary (For Viva or Long Answer)

A compiled language uses a compiler to translate the entire source code into machine code before execution, resulting in faster execution but poor portability.
An interpreted language uses an interpreter to translate and execute line-by-line, making it slower, but easier to debug and highly portable.

✅ Bonus: Hybrid Languages (Both Compiled & Interpreted)
Language	How It Works
Java	Compiled to Bytecode (.class) → then interpreted by JVM
Python (with PyPy or Cython)	Can be both interpreted or compiled */